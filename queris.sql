create view Total_Sales as
select distinct count(ParliamentConstituency) from constituencywise_results;

#2.total no of seats available for election in each state
create view seats_in_eachstate as
select s.state as State_name,count(cr.ParliamentConstituency) as Total_Seats 
from constituencywise_results cr
inner join statewise_results sr on cr.ParliamentConstituency=sr.Parliament_Constituency
inner join states s on sr.State_ID=s.StateID
group by s.State;
select * from seats_in_eachstate;

